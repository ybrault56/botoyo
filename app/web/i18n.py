def resolve_lang(request: Request) -> str:
    """Return the UI language requested by query params, defaulting to French."""

    candidate = str(request.query_params.get("lang", DEFAULT_LANG)).strip().lower()
    return candidate if candidate in SUPPORTED_LANGS else DEFAULT_LANG


def translate(key: str, lang: str) -> str:
    """Translate one UI key for the selected language."""

    return _TRANSLATIONS.get(lang, _TRANSLATIONS[DEFAULT_LANG]).get(
        key,
        _TRANSLATIONS[DEFAULT_LANG].get(key, key),
    )


def i18n_context(request: Request, *, title_key: str) -> dict[str, Any]:
    """Return common Jinja context for translated web pages."""

    lang = resolve_lang(request)

    def t(key: str, **kwargs: Any) -> str:
        text = translate(key, lang)
        return text.format(**kwargs) if kwargs else text

    return {
        "lang": lang,
        "title_key": title_key,
        "title": translate(f"title.{title_key}", lang),
        "t": t,
        "nav_urls": {
            "dashboard": language_url("/", lang),
            "admin": language_url("/admin", lang),
            "journal": language_url("/journal", lang),
        },
        "language_switch_urls": {
            "fr": switch_language_url(request, "fr"),
            "en": switch_language_url(request, "en"),
        },
    }


def language_url(path: str, lang: str, extra: dict[str, str] | None = None) -> str:
    """Build an internal URL carrying the selected language when needed."""

    query = dict(extra or {})
    if lang != DEFAULT_LANG:
        query["lang"] = lang
    return f"{path}?{urlencode(query)}" if query else path


def switch_language_url(request: Request, lang: str) -> str:
    """Return the current path with only the language parameter changed."""

    query = dict(request.query_params)
    if lang == DEFAULT_LANG:
        query.pop("lang", None)
    else:
        query["lang"] = lang
    return f"{request.url.path}?{urlencode(query)}" if query else request.url.path
